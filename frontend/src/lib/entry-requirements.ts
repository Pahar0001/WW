export function getRegime(code: string): { kind: RegimeKind; regime: Regime; hint?: string } {
  const kind = REGIME_BY_CODE[code] ?? 'unknown';
  return { kind, regime: REGIMES[kind], hint: REGIME_HINT[code] };
}

