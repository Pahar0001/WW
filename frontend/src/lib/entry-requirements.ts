// Требования и ограничения по въезду/выезду — ориентировано на граждан РФ.
//
// Real Data Policy: мы НЕ выдумываем точные визовые правила, сроки и пошлины —
// они часто меняются. Поэтому здесь только КАТЕГОРИЯ визового режима (стабильная,
// широко задокументированная) как справочный ориентир + универсальный, честный
// чек-лист того, что нужно проверить. Конкретику (сроки, сборы, документы под
// вашу цель поездки) всегда уточняйте в официальном источнике.

export type RegimeKind =
  | 'domestic'
  | 'eaeu-id'
  | 'visa-free'
  | 'voa'
  | 'evisa'
  | 'visa'
  | 'unknown';

export interface Regime {
  label: string;
  note: string;
  /** Тон бейджа: 'ok' — проще всего, 'mid' — нужно действие, 'req' — виза заранее. */
  tone: 'ok' | 'mid' | 'req' | 'muted';
}

